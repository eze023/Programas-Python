print("Programa para verificar numeros positivos o negativos")
print()
 
num1 = int(input("Ingrese su numero: "))

if num1>0:
	print()
	print("Su numero es positivo")
elif num1<0:
	print()
	print("Su numero es negativo")
else:
	print("Su numero es 0")

print()
print("Fin del programa")

#programa basico sin control de ingreso
#si el numero ingresado es mayor a 0 es positivo, si es menor a 0 es negativo
#en caso de no cumplir ninguna de las dos operaciones logicas el numero sera 0